package com.ijcsj.mylibrary.bean;

public class LittleGooseAbout {
    private String office_url;
    private String office_image;
    private String software_name;
    private String branch_icon;
    private String company_introduct;

    public String getOffice_url() {
        return office_url;
    }

    public void setOffice_url(String office_url) {
        this.office_url = office_url;
    }

    public String getOffice_image() {
        return office_image;
    }

    public void setOffice_image(String office_image) {
        this.office_image = office_image;
    }

    public String getSoftware_name() {
        return software_name;
    }

    public void setSoftware_name(String software_name) {
        this.software_name = software_name;
    }

    public String getBranch_icon() {
        return branch_icon;
    }

    public void setBranch_icon(String branch_icon) {
        this.branch_icon = branch_icon;
    }

    public String getCompany_introduct() {
        return company_introduct;
    }

    public void setCompany_introduct(String company_introduct) {
        this.company_introduct = company_introduct;
    }
}
